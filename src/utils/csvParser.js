/**
 * CSV 텍스트를 파싱하여 지자체별 관리주체 목록을 생성합니다.
 * @param {string} csvText - CSV 파일의 텍스트 내용
 * @returns {Object} 지자체별 관리주체 목록
 */
export function parseManagementList(csvText) {
    const lines = csvText.trim().split('\n');
    const header = lines[0]; // 첫 번째 줄은 헤더
    
    // 헤더 검증
    if (!header.includes('관리계획 수립기관') || !header.includes('관리주체')) {
        throw new Error('CSV 파일 형식이 올바르지 않습니다. 헤더에 "관리계획 수립기관"과 "관리주체" 컬럼이 필요합니다.');
    }
    
    const result = {};
    
    // 데이터 라인들을 처리 (헤더 제외)
    for (let i = 1; i < lines.length; i++) {
        const line = lines[i].trim();
        if (!line) continue; // 빈 줄 건너뛰기
        
        // CSV 파싱 (쉼표로 분리)
        const columns = line.split(',');
        if (columns.length < 2) continue;
        
        const establishmentAgency = columns[0].trim(); // 관리계획 수립기관
        const managementEntity = columns[1].trim(); // 관리주체
        
        if (!establishmentAgency || !managementEntity) continue;
        
        // 지자체별로 그룹화
        if (!result[establishmentAgency]) {
            result[establishmentAgency] = [];
        }
        
        // 중복 제거
        if (!result[establishmentAgency].includes(managementEntity)) {
            result[establishmentAgency].push(managementEntity);
        }
    }
    
    // 각 지자체에 "전체" 옵션 추가
    Object.keys(result).forEach(agency => {
        if (!result[agency].includes('전체')) {
            result[agency].unshift('전체'); // 맨 앞에 "전체" 추가
        }
    });
    
    return result;
}

